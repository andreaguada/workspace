package esercitazione;

public class OperazioniSuNumeri3 {
	
	  public int numeroX;
	  public int numeroY;
	    
	  public OperazioniSuNumeri3(int x, int y) {   
	    numeroX=x;
	    numeroY=y;
	  }
	    
	  public int sottrazione() {
	    int sottrazione;
	     sottrazione=numeroX-numeroY;
	     return sottrazione;
	  }
	    
	  public int somma() {
	    int somma;
	     somma=numeroX+numeroY;
	     return somma;
	  }
	    
}
